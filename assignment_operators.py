# Assignment operators are used to assign values to variables

x = 5
y = x
print(y)  # 5

x = 5
x += 5  # Same as x = x + 5
print(x)  # 10

x = 5
x -= 3  # Same as x = x - 3
print(x)

x = 5
x *= 2  # Same as x = x * 2
print(x)

x = 5
x /= 2  # Same as x = x / 2
print(x)

x = 5
x //= 3  # Same as x = x // 3
print(x)

x = 5
x %= 4  # Same as x = x % 4
print(x)

x = 5
x **= 2  # Same as x = x ** 2
print(x)
